#include "def.h"

int main() {
    // A:101010100 - 340
    // B:000011001 - 25
    // &:000010000 - 16
    printf("AND: %d\n", 340 & 25);
    // |:101011101 - 495
    printf("OR: %d\n", 340 | 25);

    // A:000011001 - 25
    // B:101010100 - 340
    // &:000010000 - 16
    printf("AND: %d\n", 25 & 340);
    // |:101011101 - 495
    printf("OR: %d\n", 25 | 340);

    // ~:101010100 - 10101011 -341 2's complement
    printf("NOT: %d\n", ~340);

    // 000011001 ~& 101010100 -> 000011001 & 010101011
    // ~&: 000001001 - 9
    printf("NAND: %d\n", 25 & ~340);

    // 01001 ^ 10010
    // ^:100
    printf("XOR: %d\n", 9 ^ 18);

    // >> 1001 : 0100
    printf("RIGHT SHIFT: %d\n", 9 >> 1);

    // << 01001 : 10010
    printf("LEFT SHIFT: %d\n", 9 << 1);

    printf("TEST: %d\n", 217 >> 1);
}

